using System.Collections.Generic;
using Helpers;
using TaleWorlds.CampaignSystem;
using TaleWorlds.CampaignSystem.LogEntries;
using TaleWorlds.Localization;
using TaleWorlds.ObjectSystem;
using TaleWorlds.SaveSystem;

namespace GreyWardenPolicePurity
{
    internal sealed class GreyWardenAdoptionLogEntry : LogEntry, IChatNotification, IEncyclopediaLog
    {
        [SaveableField(1)]
        public readonly Hero AdoptedHero;

        [SaveableField(2)]
        private readonly string _originVillageName;

        public GreyWardenAdoptionLogEntry(Hero adoptedHero, string originVillageName)
        {
            AdoptedHero = adoptedHero;
            _originVillageName = originVillageName ?? string.Empty;
        }

        public bool IsVisibleNotification => true;

        public override CampaignTime KeepInHistoryTime =>
            CampaignTime.Years(Campaign.Current.Models.AgeModel.BecomeOldAge / 2f);

        public override ChatNotificationType NotificationType => PoliticalNotification(AdoptedHero?.Clan);

        internal static void AutoGeneratedStaticCollectObjectsGreyWardenAdoptionLogEntry(
            object o,
            List<object> collectedObjects)
        {
            ((GreyWardenAdoptionLogEntry)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
        }

        protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
        {
            base.AutoGeneratedInstanceCollectObjects(collectedObjects);
            collectedObjects.Add(AdoptedHero);
        }

        internal static object AutoGeneratedGetMemberValueAdoptedHero(object o)
        {
            return ((GreyWardenAdoptionLogEntry)o).AdoptedHero;
        }

        internal static object AutoGeneratedGetMemberValue_originVillageName(object o)
        {
            return ((GreyWardenAdoptionLogEntry)o)._originVillageName;
        }

        public TextObject GetNotificationText()
        {
            return BuildText("{=gwp_adoption_chat_notice}{HERO.LINK}被灰袍守卫从{VILLAGE}收留了。");
        }

        public bool IsVisibleInEncyclopediaPageOf<T>(T obj) where T : MBObjectBase
        {
            MBObjectBase? entryTarget = obj;
            return entryTarget == AdoptedHero || entryTarget == AdoptedHero?.Clan;
        }

        public TextObject GetEncyclopediaText()
        {
            return BuildText("{=gwp_adoption_log_entry}{HERO.LINK}被灰袍守卫从{VILLAGE}收留，并带回内院抚养。");
        }

        public override string ToString()
        {
            return GetEncyclopediaText().ToString();
        }

        private TextObject BuildText(string template)
        {
            TextObject text = new TextObject(template);
            if (AdoptedHero?.CharacterObject != null)
            {
                StringHelpers.SetCharacterProperties("HERO", AdoptedHero.CharacterObject, text);
            }

            text.SetTextVariable("VILLAGE", string.IsNullOrWhiteSpace(_originVillageName) ? "未知村庄" : _originVillageName);
            return text;
        }
    }
}
